imin=8;imax=40;%p13为平稳高低级都采用斜齿轮
nmin=nw*imin;nmax=nw*imax;
sprintf('总传动范围在%.2d-%.2d之间',nmin,nmax)
%总传动范围在611-3060之间,舍去边值，结合之前计算的功率，P328满足要求的如下：
%型号      额定功率 额定转速 额定转矩 最大转矩 质量
