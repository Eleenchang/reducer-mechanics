P0=Pw/eta;
sprintf('电动机输出功率%.2dkW',P0)
%3.41kW
%由于载荷平稳，电动机额定功率Pm只需略大于P0（一般3.41到3.41*1.3=4.4330）
%% 确定电动机转速
