%电动机向低速的工作轴各级传动比依次增大较为有利p19，对于两级展开式圆柱齿轮减速器，If=1.3-1.6Is
Ismin=sqrt(i/1.6);Ifmax=Ismin*1.6;
Ismax=sqrt(i/1.3);Ifmin=Ismax*1.3;
sprintf('极限传动比为%0.2d(%0.2d)-%0.2d(%0.2d)',Ismin,Ifmax,Ismax,Ifmin)
%极限传动比为3.43(5.49)-3.81(4.95)
